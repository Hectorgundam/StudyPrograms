    // START - Load Movie Data 
    private void loadMovieData() {
        
        try (Connection conn = DBConnection.connect()){
            
            String sql = "SELECT * FROM Movies WHERE id = ?"; 
            PreparedStatement stmt = conn.prepareStatement(sql); 
            stmt.setInt(1, movieId); 
            ResultSet rs = stmt.executeQuery(); 
            
            if (rs.next()) {
                
                txtTitle.setText(rs.getString("title"));
                txtDirector.setText(rs.getString("director"));
                txtWriters.setText(rs.getString("writers"));
                txtReleaseDate.setText(rs.getString("release_date"));
                txtDuration.setText(rs.getString("duration"));
                txtRated.setText(rs.getString("rating"));
                txtGenre.setText(rs.getString("genre"));
                txtCast.setText(rs.getString("cast"));
                txtPricePerDay.setText(rs.getString("price_per_day"));
                txtDVDUnits.setText(rs.getString("dvd_units"));
                txtBluRayUnits.setText(rs.getString("blu_ray_units"));
               
            }
            
            
            
        } catch (Exception e) {
            
            e.printStackTrace(); 
            JOptionPane.showMessageDialog(this, "Error loading movie data.");
            
            
        }
        
        
    }
    // END - Load Movie Data
    
    
    

