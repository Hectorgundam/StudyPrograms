    // START - Save button
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        try (Connection conn = DBConnection.connect()) {
            
            String sql; 
            
            if (movieId == null){
                
                sql = "INSERT INTO Movies (title, director, writers, release_date, duration, rating, genre, cast, price_per_day, poster_path, dvd_units, blu_ray_units)" + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
               
            } else {
                
                sql = "UPDATE Movies SET title=?, director=?, writers=?, release_date=?, duration=?, rating=?, genre=?, cast=?, price_per_day=?, poster_path=?, dvd_units=?, blu_ray_units=?" + "WHERE id=?";
                
            }
            
            PreparedStatement stmt = conn.prepareStatement(sql); 
            stmt.setString(1, txtTitle.getText());
            stmt.setString(2, txtDirector.getText());
            stmt.setString(3, txtWriters.getText());
            stmt.setString(4, txtReleaseDate.getText());
            stmt.setString(5, txtDuration.getText());
            stmt.setString(6, txtRated.getText());
            stmt.setString(7, txtGenre.getText());
            stmt.setString(8, txtCast.getText());
            stmt.setDouble(9, Double.parseDouble(txtPricePerDay.getText()));
            stmt.setString(10, lblPoster.getText());
            stmt.setString(11, txtDVDUnits.getText());
            stmt.setString(12, txtBluRayUnits.getText());
            
            if (movieId != null) {
                
                stmt.setInt(13, movieId);
                
            }
           
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Movie data saved."); 
            parent.loadMovies(); 
            this.dispose();
            
        } catch (Exception e) {
            
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving movie data.");
                        
        }   
        
    }//GEN-LAST:event_btnSaveActionPerformed
