    private void loadRentalDetails() {
        
        try (Connection conn = DBConnection.connect()){
            
            String sql = "SELECT Rentals.*, Movies.title, Movies.price_per_day, Users.first_name, Users.last_name " + 
                         "FROM Rentals " + 
                         "JOIN Movies ON Rentals.movie_id = Movies.id " +
                         "JOIN Users ON Rentals.customer_id = Users.id " +
                         "WHERE Rentals.movie_id = ? AND Rentals.customer_id = ?"; 
            
            PreparedStatement stmt = conn.prepareStatement(sql); 
            stmt.setInt(1, movieId);
            stmt.setInt(2, customerId);
            
            ResultSet rs = stmt.executeQuery(); 
            
            if (rs.next()) {
                
                txtMovieTitle.setText(rs.getString("title"));
                txtMovieFormat.setText(rs.getString("format"));
                txtCustomerName.setText(rs.getString("first_name") + " " + rs.getString("last_name"));
                txtRentDate.setText(rs.getString("rent_date"));
                txtReturnDate.setText(rs.getString("return_date")); 
                txtPricePerDay.setText(rs.getString("price_per_day"));
                
                java.sql.Date rentDate = rs.getDate("rent_date"); 
                java.sql.Date returnDate = rs.getDate("return_date"); 
                long diffInMillis = returnDate.getTime() - rentDate.getTime(); 
                long days = (diffInMillis / ( 1000 * 60 * 60 * 24)) + 1; 
                txtDaysRented.setText(String.valueOf(days)); 
                
                double pricePerDay = rs.getDouble("price_per_day"); 
                double totalCost = days * pricePerDay; 
                txtTotalCost.setText(String.format("%.2f", totalCost)); 
                
            } else { 
                
                JOptionPane.showMessageDialog(this, "Unable to locate rental.");
                this.dispose();
                
            }
            
        } catch (Exception e) {
            
            e.printStackTrace(); 
            JOptionPane.showMessageDialog(this, "Unable to load rental details.");
            
        }
        
    }

