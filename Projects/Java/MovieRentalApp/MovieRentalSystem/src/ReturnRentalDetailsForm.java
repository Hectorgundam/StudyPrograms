    private void btnConfirmReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmReturnActionPerformed
        // TODO add your handling code here:
        
        try (Connection conn = DBConnection.connect()) {
            
            String updateRentalSql = "UPDATE Rentals SET return_date = ?, status = 'Returned' WHERE movie_id = ? AND customer_id = ?"; 
            PreparedStatement rentalStmt = conn.prepareStatement(updateRentalSql); 
            rentalStmt.setDate(1, new java.sql.Date(System.currentTimeMillis())); 
            rentalStmt.setInt(2, movieId);
            rentalStmt.setInt(3, customerId);
            rentalStmt.executeUpdate(); 
            
            String format = txtMovieFormat.getText(); 
            
            String updateUnitsSql; 
            
            if (format.equalsIgnoreCase("DVD")){ 
                
                updateUnitsSql = "UPDATE Movies SET dvd_units = dvd_units + 1 WHERE id = ?"; 
                
            } else {
                
                updateUnitsSql = "UPDATE Movies SET blu_ray_units = blu_ray_units + 1 WHERE id = ?"; 
                
            }
            
            PreparedStatement unitStmt = conn.prepareStatement(updateUnitsSql); 
            unitStmt.setInt(1, movieId); 
            unitStmt.executeUpdate(); 
            
            JOptionPane.showMessageDialog(this, "Rental successully returned.");
            
            if (parent != null) { 
                
                parent.loadRentals();
                
            }
            
            this.dispose();
            
        } catch (Exception e) {
            
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Unable to process return.");
            
        } 
        
    }//GEN-LAST:event_btnConfirmReturnActionPerformed

