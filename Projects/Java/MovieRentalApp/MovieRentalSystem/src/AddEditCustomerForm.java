    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        try (Connection conn = DBConnection.connect()) {
            
            String sql; 
        
            if (customerId == null) {
                sql = "INSERT INTO Users (first_name, middle_name, last_name, address, city, zip_code, phone_number, dob, role)" + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)"; 

            } else {
                sql = "UPDATE Users SET first_name=?, middle_name=?, last_name=?, address=?, city=?, zip_code=?, phone_number=?, dob=?, role=?" + "WHERE id=?";
            }

            PreparedStatement stmt = conn.prepareStatement(sql); 
            stmt.setString(1, txtFirstName.getText());
            stmt.setString(2,txtMiddleName.getText()); 
            stmt.setString(3, txtLastName.getText()); 
            stmt.setString(4, txtAddress.getText()); 
            stmt.setString(5, txtCity.getText()); 
            stmt.setString(6, txtZipCode.getText()); 
            stmt.setString(7, txtPhone.getText()); 
            stmt.setString(8, txtDoB.getText()); 
            stmt.setString(9, comboRole.getSelectedItem().toString()); 
            
            if (customerId != null) {
                stmt.setInt(10, customerId); 
            }
            
            stmt.executeUpdate(); 
            
            JOptionPane.showMessageDialog(this, "Customer data saved successfully!");
            parent.refreshCustomerTable(); 
            this.dispose(); 
            
        } catch (Exception e) {
            
            e.printStackTrace(); 
            JOptionPane.showMessageDialog(this, "Unable to save customer data.");
            
        }
        
        
        
        
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

