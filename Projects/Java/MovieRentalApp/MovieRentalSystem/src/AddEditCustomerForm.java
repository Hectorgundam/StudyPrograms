    private void loadCustomerData() {
        
        try (Connection conn = DBConnection.connect()) {
            
            String sql = "SELECT * FROM Users WHERE id = ?"; 
            
            PreparedStatement stmt = conn.prepareStatement(sql); 
            stmt.setInt(1, customerId);
            ResultSet rs = stmt.executeQuery();
            
            if (rs.next()) {
                
                txtFirstName.setText(rs.getString("first_name"));
                txtMiddleName.setText(rs.getString("middle_name"));
                txtLastName.setText(rs.getString("last_name"));
                txtAddress.setText(rs.getString("address")); 
                txtCity.setText(rs.getString("city"));
                txtZipCode.setText(rs.getString("zip_code"));
                txtPhone.setText(rs.getString("phone_number"));
                txtDoB.setText(rs.getString("dob"));
                comboRole.setSelectedItem(rs.getString("role"));
                
            }
            
            
        } catch (Exception e) {
            
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading customer data.");
            
        }
        
        
    }
